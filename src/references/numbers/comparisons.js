// Greater than
// logs true
console.log(2 > 1);

// Less than
// logs false
console.log(2 < 1);

// Equals
// logs true
console.log(2 === 2);

// Does not equal
// logs true
console.log(2 !== 1);

// Greater than or equal to
// logs true
console.log(2 >= 1);

// Less than or equal to
// logs true
console.log(2 <= 2);
